				<h3>OUR STORY</h3>
				
			<p><img src="https://cdn.shopify.com/s/files/1/0522/5152/5297/files/company-01.jpg?v=1614827695" class="picture">
			Sulci is an "ethical and fair trade" bag brand that was launched in the summer of 2012. The brand's founder spent two years in the Philippines, specifically in Cebu and Bohol, teaching local women how to crochet, design bags, and providing technical guidance. The goal of Sulci is to create sustainable employment opportunities for local women who may not have jobs, allowing them to work while managing household responsibilities. Sulci aims to empower these women, boost their self-confidence, and highlight the potential and resources available in developing countries. The brand is committed to supporting its producers, improving bag design and quality, and making their products accessible to a wider customer base. Sulci introduced the use of raffia for crochet in the Philippines, offering a flexible working environment that doesn't require heavy machinery or a specific location. 
