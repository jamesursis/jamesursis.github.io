			<h3>FAIR TRADE</h3>
			<p>
			Sulci provides crocheters with place of work and environment wherein they can receive proper compensation for their work and live a life with pride, at the same time as doing housework and raising children. Money they earn be spent for daily life, education for children, purchase of domestic animal and so on. Local women are now working with pride and a sense of responsibility by taking charge of management and accounting of the workshop. Previously having to crochet esparilles shoes for less than P20 (Philippine peso) per pair, now at Sulci they are earning between P200 and P900, depending on the bag’s design. n one week, a crocheter can produce three bags. This means women, who are doing the crochet inside their homes, can get P2000 in a week.
