			<h3>SUSTAINABLE PRODUCTION</h3>
			<p><img src="https://cdn.shopify.com/s/files/1/0522/5152/5297/files/english-03.jpg?v=1614827696" class="picture1"><br>
			Sulci bags are crocheted from the fiber of Raffia (a kind of palm tree and known locally as “buli”). In the Philippines, Raffia predominantly grows in colonies such as the island of Bohol. With an abundance Raffia trees, the fiber is harvested creating, employment. Some of the benefits of Raffia include Warmth pertinent to to the handmade natural material, durability, flexibility and light weight. In Japan Sulci bags are being sold at department stores from Tokyo to various localities and receiving high opinion.
			</p>
