			<h3>HISTORY</h3>
			<p><img src="https://cdn.shopify.com/s/files/1/0522/5152/5297/files/english-02.jpg?v=1614827696" class="picture">
			The Sulci story started when Satomi Sekiya who has always been an expart and a hobbyist in crochet and now a Sulci representative, visited the Philippines in 2010 and saw the situation of local women, who were willing to work but could not find a place of work. Sekiya then decided to develop a way for local women to earn income and be independent through bag making. It has been nearly two years since she taught local women crocheting skills from the basics, such as how to hold a crochet hook and explaining crocheting patterns. Sulci built up its trust and relationship with local women increasing quality to the point where it began to sell its products in 2012. It was Sulci that introduced the use of Raffia for crocheting with a crochet hook as it had traditionally been used for weaving only. This method allowed the freedom of working from home, relieving the requirement for investment in machinery or a factory.
			</p>
