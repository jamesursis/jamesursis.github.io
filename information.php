				<h7> Career</h7><br><br><br>
				<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; After quitting my previous job and traveling to the Philippines to refresh myself, I saw some handmade table mats and baskets being sold at a souvenir shop, and suddenly wondered how much the makers were paid. . If you calculate backwards from the price, you shouldn't have received much. At that time, I thought, ``If I could get people to make the products I designed directly and develop a sales channel, I could start a business that would pay the creators properly.'' ``I started making hand-woven bags using the natural material raffia.'' ``I want to try it.'' That feeling led me to launch the ``Sulusi'' bag brand. Without thinking about the future, I started knitting training with just a dozen people in Cebu and Bohol. I started teaching women who were beginners at knitting from scratch, from how to hold a crochet hook to explaining the patterns, but when I started teaching, it was difficult to make the bags I wanted, and my energy, physical strength, and funds were running low. More than once, I was worried that it might not last. “Now that we have taken responsibility for the producers, there is no going back.” “If we make good products, the customers will understand.” “If we work hard, we will find a way.” With this unwavering feeling and belief alone, we can encourage women. Continue to provide technical guidance. On the other hand, with the support of the hard work and smiles of the girls who have followed me without any doubts, I feel that the ``Sulusi'' bag has taken shape step by step. Gradually, he built a relationship of trust with the women, and once they learned that ``proper knitting can lead to high wages,'' his skill acquisition accelerated and he was able to sell at Japanese department stores. Surushi was established in November 2011 when quality was established and supply prospects were established. He also serves as a bag designer. He travels back and forth between Japan and the Philippines.</p>
